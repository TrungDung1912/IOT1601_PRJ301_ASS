package model;

/**
 *
 * @author ADMIN
 */
public class Student {
    private String id;
    private String name;
    private String slot;

    public Student() {
    }

    public Student(String id, String name, String slot) {
        this.id = id;
        this.name = name;
        this.slot = slot;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSlot() {
        return slot;
    }

    public void setSlot(String slot) {
        this.slot = slot;
    }
    
    
}
